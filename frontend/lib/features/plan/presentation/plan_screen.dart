  }
}

class _AddTaskSheet extends StatefulWidget {
  const _AddTaskSheet();
  @override State<_AddTaskSheet> createState() => _AddTaskSheetState();
}

class _AddTaskSheetState extends State<_AddTaskSheet> {
  final _textCtrl = TextEditingController();
  final _linkCtrl = TextEditingController();
  String _platform = 'LeetCode', _priority = 'medium';
  final int _minutes = 30;

  @override
  Widget build(BuildContext context) {
    final isDark = Theme.of(context).brightness == Brightness.dark;
    return Container(
      decoration: BoxDecoration(color: isDark ? AppColors.darkCard : Colors.white, borderRadius: const BorderRadius.vertical(top: Radius.circular(20))),
      padding: EdgeInsets.only(left: 20, right: 20, top: 20, bottom: MediaQuery.of(context).viewInsets.bottom + 20),
      child: Column(mainAxisSize: MainAxisSize.min, crossAxisAlignment: CrossAxisAlignment.start, children: [
        Text('Add Task', style: Theme.of(context).textTheme.titleLarge?.copyWith(fontWeight: FontWeight.w700, color: isDark ? Colors.white : AppColors.lightText)),
        const SizedBox(height: 16),
        TextFormField(controller: _textCtrl, style: TextStyle(color: isDark ? Colors.white : AppColors.lightText), decoration: const InputDecoration(labelText: 'Task description')),
        const SizedBox(height: 12),
        TextFormField(controller: _linkCtrl, style: TextStyle(color: isDark ? Colors.white : AppColors.lightText), decoration: const InputDecoration(labelText: 'Link (optional)')),
        const SizedBox(height: 12),
        Row(children: [
          Expanded(child: DropdownButtonFormField<String>(initialValue: _priority, onChanged: (v) => setState(() => _priority = v!),
            style: TextStyle(color: isDark ? Colors.white : AppColors.lightText), dropdownColor: isDark ? AppColors.darkCard : Colors.white,
            decoration: const InputDecoration(labelText: 'Priority'),
            items: ['low','medium','high'].map((p) => DropdownMenuItem(value: p, child: Text(p[0].toUpperCase() + p.substring(1), style: TextStyle(color: isDark ? Colors.white : AppColors.lightText)))).toList())),
          const SizedBox(width: 12),
          Expanded(child: DropdownButtonFormField<String>(initialValue: _platform, onChanged: (v) => setState(() => _platform = v!),
            style: TextStyle(color: isDark ? Colors.white : AppColors.lightText), dropdownColor: isDark ? AppColors.darkCard : Colors.white,
            decoration: const InputDecoration(labelText: 'Platform'),
            items: AppConstants.platforms.map((p) => DropdownMenuItem(value: p, child: Text(p, style: TextStyle(color: isDark ? Colors.white : AppColors.lightText)))).toList())),
        ]),
        const SizedBox(height: 16),
        OwlButton(label: 'Add Task', onTap: _textCtrl.text.isNotEmpty ? _submit : null),
      ]),
    );
  }

  void _submit() {
    if (_textCtrl.text.trim().isEmpty) return;
    context.read<PlanBloc>().add(PlanTaskAdded({'text': _textCtrl.text.trim(), 'platform': _platform, 'priority': _priority, 'link': _linkCtrl.text.trim(), 'estimatedMinutes': _minutes}));
    Navigator.pop(context);
  }
}
