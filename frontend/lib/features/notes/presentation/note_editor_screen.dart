  Widget _buildMarkdownPreview() {
    final lines = _contentCtrl.text.split('\n');
    return Container(
      padding: const EdgeInsets.all(12),
      decoration: BoxDecoration(color: const Color(0xFF0D0D1A), borderRadius: BorderRadius.circular(12)),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: lines.map((line) {
          if (line.startsWith('# ')) return Padding(padding: const EdgeInsets.only(bottom: 8, top: 12), child: Text(line.substring(2), style: const TextStyle(color: Colors.white, fontSize: 22, fontWeight: FontWeight.w800)));
          if (line.startsWith('## ')) return Padding(padding: const EdgeInsets.only(bottom: 6, top: 8), child: Text(line.substring(3), style: const TextStyle(color: Colors.white, fontSize: 18, fontWeight: FontWeight.w700)));
          if (line.startsWith('### ')) return Padding(padding: const EdgeInsets.only(bottom: 4, top: 6), child: Text(line.substring(4), style: const TextStyle(color: AppColors.primary, fontSize: 15, fontWeight: FontWeight.w600)));
          if (line.startsWith('- [ ] ')) return Row(children: [Checkbox(value: false, onChanged: (_) {}, fillColor: WidgetStateProperty.all(AppColors.darkCard)), Text(line.substring(6), style: const TextStyle(color: Colors.white70, fontSize: 14))]);
          if (line.startsWith('- [x] ')) return Row(children: [Checkbox(value: true, onChanged: (_) {}, fillColor: WidgetStateProperty.all(AppColors.primary)), Text(line.substring(6), style: const TextStyle(color: Colors.white54, fontSize: 14, decoration: TextDecoration.lineThrough))]);
          if (line.startsWith('- ')) return Padding(padding: const EdgeInsets.only(bottom: 2), child: Row(crossAxisAlignment: CrossAxisAlignment.start, children: [const Text('• ', style: TextStyle(color: AppColors.accent, fontSize: 14)), Expanded(child: Text(line.substring(2), style: const TextStyle(color: Colors.white70, fontSize: 14)))]));
          if (line.startsWith('> ')) return Container(margin: const EdgeInsets.only(bottom: 4), padding: const EdgeInsets.fromLTRB(12, 4, 4, 4), decoration: const BoxDecoration(border: Border(left: BorderSide(color: AppColors.primary, width: 3))), child: Text(line.substring(2), style: const TextStyle(color: Colors.white60, fontStyle: FontStyle.italic)));
          if (line.trim() == '---') return const Divider(color: AppColors.darkBorder, height: 24);
          if (line.startsWith('```')) return Container(margin: const EdgeInsets.only(bottom: 4), padding: const EdgeInsets.all(8), decoration: BoxDecoration(color: Colors.black, borderRadius: BorderRadius.circular(6)), child: Text(line, style: const TextStyle(color: Colors.greenAccent, fontSize: 12, fontFamily: 'monospace')));
          return Padding(padding: const EdgeInsets.only(bottom: 2), child: Text(line, style: const TextStyle(color: Colors.white70, fontSize: 14, height: 1.5)));
        }).toList(),
      ),
    );
  }

  Widget _buildAiPanel() {
    return Column(
      children: [
        // AI Panel Tabs
        Container(
          color: const Color(0xFF0D0D1A),
          child: TabBar(
            controller: _panelTab,
            labelColor: AppColors.accent,
            unselectedLabelColor: Colors.white38,
