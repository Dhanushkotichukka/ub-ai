  Widget _buildQuizTab() {
    if (_quiz.isEmpty) {
      return Center(
        child: Column(mainAxisAlignment: MainAxisAlignment.center, children: [
          const Text('❓', style: TextStyle(fontSize: 48)),
          const SizedBox(height: 12),
          const Text('No quiz yet', style: TextStyle(color: Colors.white, fontSize: 16)),
          const SizedBox(height: 8),
          ElevatedButton.icon(onPressed: () => _runAiAction('quiz'), icon: const Icon(Icons.auto_awesome), label: const Text('Generate Quiz'), style: ElevatedButton.styleFrom(backgroundColor: AppColors.primary)),
        ]),
      );
    }
    final q = _quiz[_quizIndex];
    final options = List<String>.from(q['options'] ?? ['True', 'False']);
    final correct = q['answer'] ?? '';

    return SingleChildScrollView(
      padding: const EdgeInsets.all(16),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Row(children: [
            Text('Q ${_quizIndex + 1}/${_quiz.length}', style: const TextStyle(color: AppColors.darkTextSecondary, fontSize: 12)),
            const Spacer(),
            Container(padding: const EdgeInsets.symmetric(horizontal: 8, vertical: 3), decoration: BoxDecoration(color: AppColors.accent.withValues(alpha: 0.1), borderRadius: BorderRadius.circular(8)), child: Text(q['type'] ?? 'mcq', style: const TextStyle(color: AppColors.accent, fontSize: 10))),
          ]),
          const SizedBox(height: 12),
          Text(q['question'] ?? '', style: const TextStyle(color: Colors.white, fontSize: 15, fontWeight: FontWeight.w600, height: 1.5)),
          const SizedBox(height: 16),
          ...options.map((opt) {
            final isSelected = _quizSelected == opt;
            final isCorrect = opt == correct;
            Color borderColor = AppColors.darkBorder;
            Color bgColor = const Color(0xFF1A1A2E);
            if (_quizShowAnswer) {
              if (isCorrect) { borderColor = AppColors.success; bgColor = AppColors.success.withValues(alpha: 0.1); }
              else if (isSelected && !isCorrect) { borderColor = AppColors.danger; bgColor = AppColors.danger.withValues(alpha: 0.1); }
            } else if (isSelected) {
              borderColor = AppColors.primary;
              bgColor = AppColors.primary.withValues(alpha: 0.1);
            }
            return GestureDetector(
              onTap: _quizShowAnswer ? null : () => setState(() => _quizSelected = opt),
              child: Container(
                margin: const EdgeInsets.only(bottom: 8),
                padding: const EdgeInsets.all(12),
                decoration: BoxDecoration(color: bgColor, borderRadius: BorderRadius.circular(10), border: Border.all(color: borderColor)),
                child: Text(opt, style: TextStyle(color: _quizShowAnswer && isCorrect ? Colors.white : Colors.white70, fontWeight: _quizShowAnswer && isCorrect ? FontWeight.w600 : FontWeight.normal, fontSize: 13)),
              ),
            );
          }),
          const SizedBox(height: 12),
          if (!_quizShowAnswer)
            SizedBox(width: double.infinity, child: ElevatedButton(onPressed: _quizSelected != null ? () => setState(() => _quizShowAnswer = true) : null, style: ElevatedButton.styleFrom(backgroundColor: AppColors.primary), child: const Text('Check Answer')))
          else ...[
            if (q['explanation'] != null)
              Container(padding: const EdgeInsets.all(12), decoration: BoxDecoration(color: AppColors.success.withValues(alpha: 0.05), borderRadius: BorderRadius.circular(10), border: Border.all(color: AppColors.success.withValues(alpha: 0.3))), child: Text('💡 ${q['explanation']}', style: const TextStyle(color: Colors.white70, fontSize: 12, height: 1.4))),
            const SizedBox(height: 10),
            Row(children: [
              if (_quizIndex < _quiz.length - 1)
                Expanded(child: ElevatedButton(onPressed: () => setState(() { _quizIndex++; _quizSelected = null; _quizShowAnswer = false; }), style: ElevatedButton.styleFrom(backgroundColor: AppColors.primary), child: const Text('Next Question'))),
              if (_quizIndex == _quiz.length - 1)
                Expanded(child: ElevatedButton(onPressed: () => setState(() { _quizIndex = 0; _quizSelected = null; _quizShowAnswer = false; }), style: ElevatedButton.styleFrom(backgroundColor: AppColors.success), child: const Text('Restart Quiz'))),
            ]),
          ],
        ],
      ),
    );
  }

