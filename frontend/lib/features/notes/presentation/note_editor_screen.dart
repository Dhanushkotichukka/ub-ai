// ─── Note Editor Screen ───────────────────────────────────────────
class NoteEditorScreen extends StatefulWidget {
  final String? noteId;
  final String? initialNoteType;
  const NoteEditorScreen({super.key, this.noteId, this.initialNoteType});
  @override State<NoteEditorScreen> createState() => _NoteEditorScreenState();
}

class _NoteEditorScreenState extends State<NoteEditorScreen> with SingleTickerProviderStateMixin {
  final _titleCtrl = TextEditingController();
  final _contentCtrl = TextEditingController();
  final _tagsCtrl = TextEditingController();
  final _folderCtrl = TextEditingController();
  final _chatCtrl = TextEditingController();
  final ScrollController _chatScrollCtrl = ScrollController();
  late TabController _panelTab;

  String _color = 'default';
  String _noteType = 'quick';
  String _collection = 'General';
  bool _isPinned = false;
  bool _isFavorited = false;
  bool _loading = false;
  bool _saving = false;
  bool _previewMode = false;
  int _wordCount = 0;

  // AI state
  bool _aiLoading = false;
  String _aiAction = '';
  String _aiResponse = '';
  List<Map<String, dynamic>> _flashcards = [];
  List<Map<String, dynamic>> _quiz = [];
  List<Map<String, dynamic>> _chatHistory = [];
  String _roadmap = '';
  int _flashcardIndex = 0;
  bool _flashcardFlipped = false;
  int _quizIndex = 0;
  String? _quizSelected;
  bool _quizShowAnswer = false;
  Timer? _autoSaveTimer;

  final List<String> _colors = ['default', 'red', 'blue', 'green', 'purple', 'orange', 'yellow', 'pink', 'teal'];
  final List<String> _collections = ['General', 'DSA', 'Flutter', 'Linux', 'ServiceNow', 'Azure', 'Interview Prep', 'Personal Journal'];

  @override
  void initState() {
    super.initState();
    _panelTab = TabController(length: 4, vsync: this);
    _noteType = widget.initialNoteType ?? 'quick';
    if (widget.noteId != null) {
      _loadNote();
    } else {
      _folderCtrl.text = 'General';
      _applyTemplate(_noteType);
    }
    _contentCtrl.addListener(_onContentChanged);
  }

  void _onContentChanged() {
    final words = _contentCtrl.text.trim().split(RegExp(r'\s+')).where((w) => w.isNotEmpty).length;
    if (words != _wordCount) setState(() => _wordCount = words);
    _autoSaveTimer?.cancel();
    _autoSaveTimer = Timer(const Duration(seconds: 3), () { if (widget.noteId != null) _saveNote(silent: true); });
  }

  void _applyTemplate(String type) {
    switch (type) {
      case 'dsa': _contentCtrl.text = kDsaTemplate; break;
      case 'meeting': _contentCtrl.text = kMeetingTemplate; break;
      case 'snippet': _contentCtrl.text = kSnippetTemplate; break;
      case 'study': _contentCtrl.text = kStudyTemplate; break;
      default: break;
    }
  }

  @override
  void dispose() {
    _autoSaveTimer?.cancel();
    _titleCtrl.dispose();
    _contentCtrl.dispose();
    _tagsCtrl.dispose();
    _folderCtrl.dispose();
    _chatCtrl.dispose();
    _chatScrollCtrl.dispose();
    _panelTab.dispose();
    super.dispose();
  }

  Future<void> _loadNote() async {
    setState(() => _loading = true);
    try {
      final note = await sl<NotesRepository>().getNote(widget.noteId!);
      setState(() {
        _titleCtrl.text = note['title'] ?? '';
        _contentCtrl.text = note['contentText'] ?? '';
        _folderCtrl.text = note['folderPath'] ?? 'General';
        _color = note['color'] ?? 'default';
        _noteType = note['noteType'] ?? 'quick';
        _collection = note['collection'] ?? 'General';
        _isPinned = note['isPinned'] ?? false;
        _isFavorited = note['isFavorited'] ?? false;
        _tagsCtrl.text = List<String>.from(note['tags'] ?? []).join(', ');
        _flashcards = List<Map<String, dynamic>>.from(note['flashcards'] ?? []);
        _quiz = List<Map<String, dynamic>>.from(note['quiz'] ?? []);
        _roadmap = note['aiRoadmap'] ?? '';
        _chatHistory = List<Map<String, dynamic>>.from(note['aiChatHistory'] ?? []);
      });
    } catch (e) {
      if (mounted) ScaffoldMessenger.of(context).showSnackBar(SnackBar(content: Text('Error: $e'), backgroundColor: AppColors.danger));
    } finally {
      setState(() => _loading = false);
    }
  }

  Future<void> _saveNote({bool silent = false}) async {
    if (_titleCtrl.text.trim().isEmpty && !silent) {
      ScaffoldMessenger.of(context).showSnackBar(const SnackBar(content: Text('Please enter a title'), backgroundColor: AppColors.warning));
      return;
    }
    if (silent && _titleCtrl.text.trim().isEmpty) return;

    setState(() => _saving = true);
    final tags = _tagsCtrl.text.split(',').map((t) => t.trim()).where((t) => t.isNotEmpty).toList();
    final contentDelta = {'ops': [{'insert': _contentCtrl.text}]};
    final data = {
      'title': _titleCtrl.text.trim().isEmpty ? 'Untitled' : _titleCtrl.text.trim(),
      'content': contentDelta,
      'folder': _folderCtrl.text.trim(),
      'folderPath': _folderCtrl.text.trim(),
      'collection': _collection,
      'tags': tags,
      'color': _color,
      'isPinned': _isPinned,
      'isFavorited': _isFavorited,
      'noteType': _noteType,
    };
    try {
      if (widget.noteId != null) {
        await sl<NotesRepository>().updateNote(widget.noteId!, data);
      } else {
        await sl<NotesRepository>().createNote(data);
      }
      if (mounted) {
        context.read<NotesBloc>().add(const NotesLoadRequested());
        if (!silent) context.pop();
      }
    } catch (e) {
      if (mounted && !silent) ScaffoldMessenger.of(context).showSnackBar(SnackBar(content: Text('Error: $e'), backgroundColor: AppColors.danger));
    } finally {
      setState(() => _saving = false);
    }
  }

  Future<void> _runAiAction(String action) async {
    final content = _contentCtrl.text.trim();
    if (content.isEmpty) {
      ScaffoldMessenger.of(context).showSnackBar(const SnackBar(content: Text('Write some content first'), backgroundColor: AppColors.warning));
      return;
    }
    if (widget.noteId == null) {
      ScaffoldMessenger.of(context).showSnackBar(const SnackBar(content: Text('Save the note first'), backgroundColor: AppColors.warning));
      return;
    }

    setState(() { _aiLoading = true; _aiAction = action; _aiResponse = ''; });

    try {
      switch (action) {
        case 'flashcards':
          final cards = await sl<NotesRepository>().generateFlashcards(widget.noteId!);
          setState(() { _flashcards = cards; _flashcardIndex = 0; _flashcardFlipped = false; _panelTab.index = 1; });
          break;
        case 'quiz':
          final q = await sl<NotesRepository>().generateQuiz(widget.noteId!);
          setState(() { _quiz = q; _quizIndex = 0; _quizSelected = null; _quizShowAnswer = false; _panelTab.index = 2; });
          break;
        case 'roadmap':
          final r = await sl<NotesRepository>().generateRoadmap(widget.noteId!);
          setState(() { _roadmap = r; _panelTab.index = 3; });
          break;
        case 'autotag':
          final result = await sl<NotesRepository>().autoTag(widget.noteId!);
          if (result['title'] != null && result['title'] != _titleCtrl.text) {
            setState(() {
              _titleCtrl.text = result['title'];
              final newTags = List<String>.from(result['tags'] ?? []);
              _tagsCtrl.text = newTags.join(', ');
            });
            if (mounted) ScaffoldMessenger.of(context).showSnackBar(const SnackBar(content: Text('✨ Title and tags updated by AI!'), backgroundColor: AppColors.success));
          }
          break;
        case 'revision':
          await sl<NotesRepository>().scheduleRevision(widget.noteId!);
          if (mounted) ScaffoldMessenger.of(context).showSnackBar(const SnackBar(content: Text('📅 Revision schedule set! 1, 3, 7, 30 days'), backgroundColor: AppColors.success));
          break;
        default:
          final res = await sl<ApiService>().post('/ai/notes-assistant', data: {'action': action, 'content': content});
          final result = res.data['result'];
          if (result is List) {
            _aiResponse = result.map((item) {
              if (item is Map && item.containsKey('question')) return 'Q: ${item['question']}\nA: ${item['answer']}';
              return item.toString();
            }).join('\n\n');
          } else {
            _aiResponse = result.toString();
          }
          _panelTab.index = 0;
      }
    } catch (e) {
      if (mounted) ScaffoldMessenger.of(context).showSnackBar(SnackBar(content: Text('AI Error: $e'), backgroundColor: AppColors.danger));
    } finally {
      setState(() => _aiLoading = false);
    }
  }

  Future<void> _sendChatMessage() async {
    final msg = _chatCtrl.text.trim();
    if (msg.isEmpty) return;
    if (widget.noteId == null) {
      ScaffoldMessenger.of(context).showSnackBar(const SnackBar(content: Text('Save the note first to chat about it'), backgroundColor: AppColors.warning));
      return;
    }
    _chatCtrl.clear();
    setState(() {
      _chatHistory.add({'role': 'user', 'content': msg});
      _aiLoading = true;
    });
    try {
      final reply = await sl<NotesRepository>().aiChat(widget.noteId!, msg);
      setState(() { _chatHistory.add({'role': 'assistant', 'content': reply}); });
      await Future.delayed(const Duration(milliseconds: 100));
      if (_chatScrollCtrl.hasClients) _chatScrollCtrl.animateTo(_chatScrollCtrl.position.maxScrollExtent, duration: const Duration(milliseconds: 300), curve: Curves.easeOut);
    } catch (e) {
      setState(() { _chatHistory.add({'role': 'assistant', 'content': 'Error: $e'}); });
    } finally {
      setState(() => _aiLoading = false);
    }
  }

  void _insertText(String prefix, String suffix) {
    final text = _contentCtrl.text;
    final sel = _contentCtrl.selection;
    if (sel.isValid) {
      final selected = sel.textInside(text);
      final newText = text.replaceRange(sel.start, sel.end, '$prefix$selected$suffix');
      _contentCtrl.value = TextEditingValue(text: newText, selection: TextSelection.collapsed(offset: sel.start + prefix.length + selected.length + suffix.length));
    } else {
      final off = text.length;
      _contentCtrl.value = TextEditingValue(text: '$text$prefix$suffix', selection: TextSelection.collapsed(offset: off + prefix.length));
    }
  }

  Color _colorHex(String name) {
    const m = {'red': Colors.redAccent, 'blue': Colors.blueAccent, 'green': Colors.greenAccent, 'purple': Colors.purpleAccent, 'orange': Colors.orangeAccent, 'yellow': Colors.yellowAccent, 'pink': Colors.pinkAccent, 'teal': Colors.tealAccent};
    return m[name] ?? AppColors.darkCard;
  }

  @override
  Widget build(BuildContext context) {
    if (_loading) return const Scaffold(backgroundColor: AppColors.darkBg, body: Center(child: CircularProgressIndicator(color: AppColors.primary)));
    final isWide = MediaQuery.of(context).size.width > 700;

    return Scaffold(
      backgroundColor: AppColors.darkBg,
      appBar: _buildAppBar(),
      body: isWide
          ? Row(children: [
              Expanded(flex: 3, child: _buildEditor()),
              Container(width: 340, decoration: const BoxDecoration(border: Border(left: BorderSide(color: AppColors.darkBorder))), child: _buildAiPanel()),
            ])
          : Column(children: [Expanded(child: _buildEditor())]),
      bottomNavigationBar: isWide ? _buildStatusBar() : _buildMobileBottom(),
    );
  }

  PreferredSizeWidget _buildAppBar() {
    return AppBar(
      backgroundColor: AppColors.darkBg,
      elevation: 0,
      leading: IconButton(icon: const Icon(Icons.arrow_back, color: Colors.white), onPressed: () => context.pop()),
      title: Row(
        children: [
          _buildTypeSelector(),
          const SizedBox(width: 8),
          Expanded(
            child: TextField(
              controller: _titleCtrl,
              style: const TextStyle(color: Colors.white, fontSize: 16, fontWeight: FontWeight.w700),
              decoration: const InputDecoration(hintText: 'Note title...', hintStyle: TextStyle(color: Colors.white38), border: InputBorder.none, isDense: true),
            ),
          ),
        ],
      ),
      actions: [
        IconButton(icon: Icon(_isFavorited ? Icons.star_rounded : Icons.star_border_rounded, color: _isFavorited ? const Color(0xFFFFB300) : Colors.white54), onPressed: () => setState(() => _isFavorited = !_isFavorited), tooltip: 'Favorite'),
        IconButton(icon: Icon(_isPinned ? Icons.push_pin_rounded : Icons.push_pin_outlined, color: _isPinned ? AppColors.primary : Colors.white54), onPressed: () => setState(() => _isPinned = !_isPinned), tooltip: 'Pin'),
        IconButton(icon: Icon(_previewMode ? Icons.edit_rounded : Icons.preview_rounded, color: Colors.white70), onPressed: () => setState(() => _previewMode = !_previewMode), tooltip: _previewMode ? 'Edit' : 'Preview'),
        IconButton(
          icon: _saving ? const SizedBox(width: 18, height: 18, child: CircularProgressIndicator(strokeWidth: 2, color: Colors.white)) : const Icon(Icons.check_rounded, color: Colors.white),
          onPressed: _saving ? null : () => _saveNote(),
          tooltip: 'Save',
        ),
      ],
    );
  }

  Widget _buildTypeSelector() {
    const types = {'quick': '⚡', 'dsa': '🧠', 'daily': '📅', 'snippet': '💻', 'meeting': '📋', 'study': '📚'};
    return GestureDetector(
      onTap: () => showDialog(
        context: context,
        builder: (ctx) => AlertDialog(
          backgroundColor: const Color(0xFF1E1E2E),
          title: const Text('Note Type', style: TextStyle(color: Colors.white)),
          content: Wrap(
            spacing: 8, runSpacing: 8,
            children: types.entries.map((e) => GestureDetector(
              onTap: () { setState(() => _noteType = e.key); Navigator.pop(ctx); },
              child: Container(
                width: 80, height: 60,
                decoration: BoxDecoration(color: _noteType == e.key ? AppColors.primary.withValues(alpha: 0.2) : const Color(0xFF1A1A2E), borderRadius: BorderRadius.circular(10), border: Border.all(color: _noteType == e.key ? AppColors.primary : AppColors.darkBorder)),
                child: Column(mainAxisAlignment: MainAxisAlignment.center, children: [
                  Text(e.value, style: const TextStyle(fontSize: 20)),
                  Text(e.key, style: const TextStyle(color: Colors.white60, fontSize: 9)),
                ]),
              ),
            )).toList(),
          ),
        ),
      ),
      child: Container(
        padding: const EdgeInsets.all(6),
        decoration: BoxDecoration(color: AppColors.primary.withValues(alpha: 0.1), borderRadius: BorderRadius.circular(8)),
        child: Text(types[_noteType] ?? '📝', style: const TextStyle(fontSize: 20)),
      ),
    );
  }

