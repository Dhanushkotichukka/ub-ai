    return SingleChildScrollView(
      padding: const EdgeInsets.all(16),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          // Metadata row
          Row(
            children: [
              const Icon(Icons.folder_rounded, size: 14, color: AppColors.darkTextSecondary),
              const SizedBox(width: 4),
              Expanded(child: TextField(controller: _folderCtrl, style: const TextStyle(color: Colors.white70, fontSize: 12), decoration: const InputDecoration(hintText: 'Folder path', hintStyle: TextStyle(color: Colors.white24, fontSize: 12), border: InputBorder.none, isDense: true))),
              const SizedBox(width: 8),
              DropdownButton<String>(
                value: _collection,
                dropdownColor: const Color(0xFF1E1E2E),
